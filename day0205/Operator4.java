/**
	관계연삱자
	>, <, >=, <=, ==, !=
*/



class Operator4 {
	public static void main(String[] args) {


		// == 두개의 값이 같았을 때 true
		System.out.println("==" + (12 == 12));
		System.out.println("==" + (12 == 7));
		// != 두개의 값이 다르면 true
		System.out.println("!=" + (12 != 12));
		System.out.println("!=" + (12 != 7));
	}
}
